    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String selectedRoom = (String)jComboBox1.getSelectedItem();
        if(selectedRoom.equalsIgnoreCase("BoardRoom"))
        {jPanel1.setBackground(Color.GRAY);}
        if(selectedRoom.equalsIgnoreCase("GreenRoom"))
        {jPanel1.setBackground(Color.GREEN);}
        if(selectedRoom.equalsIgnoreCase("BlueRoom"))
        {jPanel1.setBackground(new java.awt.Color(0, 153, 255));}
        if(selectedRoom.equalsIgnoreCase("OrangeRoom"))
        {jPanel1.setBackground(Color.ORANGE);}
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        jTextArea1.setText(null);   //clear the text area for next room's reservation
        String selectedRoom = (String)jComboBox2.getSelectedItem();
        if(selectedRoom.equalsIgnoreCase("BoardRoom"))
        {jPanel2.setBackground(Color.GRAY);FileWriterReader.toTextArea(jTextArea1, "BoardRoom");
        jTextArea1.setForeground(new java.awt.Color(102,102,102));}
        if(selectedRoom.equalsIgnoreCase("GreenRoom"))
        {jPanel2.setBackground(Color.GREEN);FileWriterReader.toTextArea(jTextArea1, "GreenRoom");
        jTextArea1.setForeground(new java.awt.Color(0,102,51));}
        if(selectedRoom.equalsIgnoreCase("BlueRoom"))
        {jPanel2.setBackground(new java.awt.Color(0, 153, 255));
        FileWriterReader.toTextArea(jTextArea1, "BlueRoom")
                ;jTextArea1.setForeground(new java.awt.Color(0,0,255));}
        if(selectedRoom.equalsIgnoreCase("OrangeRoom"))
        {jPanel2.setBackground(Color.ORANGE);FileWriterReader.toTextArea(jTextArea1, "OrangeRoom");
        jTextArea1.setForeground(new java.awt.Color(255,153,0));}
        
        //jTextArea1.setText(null);
       // FileWriterReader.toTextArea(jTextArea1, "BlueRoom");
    }//GEN-LAST:event_jComboBox2ActionPerformed

