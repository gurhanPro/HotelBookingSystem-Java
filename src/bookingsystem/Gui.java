    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        
        try {
        String date =(dateField.getDate()+"").substring(0, 10)+" "+(dateField.getDate()+"").substring(30, 34);
        String time = (String) timeField.getSelectedItem();
        String name = nameField.getText();
        String roomType = (String) jComboBox1.getSelectedItem();
        
        System.out.println(date);
        if(date.contains("Sun")|| date.contains("Sat"))
        {
            JOptionPane.showMessageDialog(null, "Sorry, We are closed on Weekends");
        }
        else if(name.length()==0){JOptionPane.showMessageDialog(null, "please fill your name");}
        else if(roomType.equalsIgnoreCase("--Select Room--")){JOptionPane.showMessageDialog(null, "please select a room");}
         
        else{
            Staff staff = new Staff(date,time,name);
            if(Room.availability(roomType,staff.toString())==true){
            Booking.bookIt(staff,roomType);
            JOptionPane.showMessageDialog(null, "room booked");}
            else{JOptionPane.showMessageDialog(null, "time slot not available , already taken");}
            
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

